<td><font size="4" color="blue"><u>Multi-role fighters and strike aircraft:</u></font>
<ul>
<li>Sukhoi Su-30MKI : The IAF's primary air superiority fighter with the additional capability to conduct air-ground (strike) missions is Sukhoi Su-30MKI. The IAF have placed an order for a total of 314 Su-30MKIs of which 242 are in service as of January 2016.</li>
<li>Mikoyan MiG-29 : The Mikoyan MiG-29 known as Baaz (Hindi for Hawk) is a dedicated air superiority fighter and constitutes a second line of defence after the Sukhoi Su-30MKI. 69 MiG-29s are in service, all of which have been recently upgraded to the MiG-29UPG standard.</li>
<li>Dassault Mirage 2000 : The Dassault Mirage 2000, known as Vajra (Sanskrit for Diamond or thunderbolt) in Indian service, is the primary multirole fighter, the IAF currently operates 49 Mirage 2000Hs and 8 Mirage 2000 TH all of which are currently being upgraded to the Mirage 2000-5 MK2 standard and 2 Mirage 2000-5 MK2 are in service as of March 2015</li>
<li>SEPECAT Jaguar: The SEPECAT Jaguar known as Shamsher serves as the IAF's primary ground attack force. The IAF currently operates 139 Jaguars.</li>
<li>Mikoyan MiG-27 : The Mikoyan MiG-27 known as Bahadur (Hindi for Valiant) serves as the IAF's primary ground attack force. The IAF currently operates over 85 MiG-27s.</li>
</ul>

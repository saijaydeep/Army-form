<b><i></h1><u><font size="6">Future of the Indian AirForce:</font></u></h1></i></b>
<table>
<tr>
<td colspan="3"><p>The number of aircraft in the IAF has been decreasing from the late 1990s due to retirement of older aircraft and several crashes. To deal with the depletion of force levels, the IAF has started to modernise its fleet. This includes both the upgrade of existing aircraft, equipment and infrastructure as well as induction of new aircraft and equipment, both indigenous and imported. As new aircraft enter service and numbers recover, the IAF plans to have a fleet of 42 squadrons.</p></td>
</tr>
<tr>
<td><font size="4" color="blue"><u>Expected future acquisitions</u></font>
<ul>
<li>Single Engined Fighters:
<ul>
<li>On 3 January 2017, Minister of Defence Manohar Parrikar addressed a media conference and announced plans for a competition to select a Strategic Partner to deliver "... 200 new single engine fighters to be made in India, which will easily cost around (USD)$45 million apiece without weaponry" with an expectation that Lockheed Martin (USA) and Saab (Sweden) will pitch the F-16 Block 70 and Gripen, respectively.</li>
<li> An MoD official said that a global tender will be put to market in the first quarter of 2018, with a private company nominated as the strategic partners production agency followed by a two or more year process to evaluate technical and financial bids and conduct trials, before the final government-to-government deal in 2021. This represents 11 squadrons of aircraft plus several 'attrition' aircraft</li>
</ul>
</li>
<li>Current acquisitions:
<ul>
<li>The IAF has placed orders for 120 HAL Tejas fighters, 36 Dassault Rafale multi-role fighters,112 Pilatus PC-7MkII basic trainers, 72 HAL HJT-36 Sitara trainers, 72 Pipistrel Virus SW 80 microlight aircraft, 10 C-17 Globemaster III strategic air-lifters, 65 HAL Light Combat Helicopters, 139 Mi-17V-5 helicopters.</li>
<li>The IAF has issued a Request for Information (RFI) for 16 C-27J Spartan medium military transport aircraft. A new Request For Information has been issued to replace Hawker Siddeley HS 748 for $2.4 billion. The IAF also submitted a request for information to international suppliers for a stealth unmanned combat air vehicle (UCAV)</li>
</ul>
</li>
<li>Network-centric warfare:
<ul>
<li>The Air Force Network (AFNET), a robust digital information grid that enabled quick and accurate threat responses, was launched in 2010, helping the IAF become a truly network-centric air force. AFNET is a secure communication network linking command and control centres with offensive aircraft, sensor platforms and ground missile batteries.</li>
<li> Integrated Air Command and Control System (IACCS), an automated system for Air Defence operations will ride the AFNet backbone integrating ground and airborne sensors, weapon systems and command and control nodes. </li>
<li>Subsequent integration with civil radar and other networks shall provide an integrated Air Situation Picture, and reportedly acts as a force multiplier for intelligence analysis, mission control, and support activities like maintenance and logistics.</li>
<li> The design features multiple layers of security measures, including encryption and intrusion prevention technologies, to hinder and deter espionage efforts</li>
</ul>
</li>
</ul>

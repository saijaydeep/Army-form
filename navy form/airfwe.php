<b><i></h1><u><font size="6">Commands:</font></u></h1></i></b>
<table>
<tr>
<td colspace="3"><p>The Indian Air Force is divided into five operational and two functional commands. Each Command is headed by an Air Officer Commanding-in-Chief with the rank of Air Marshal. The purpose of an operational command is to conduct military operations using aircraft within its area of responsibility, whereas the responsibility of functional commands is to maintain combat readiness. Aside from the Training Command at Bangalore, the primary flight training is done at the Air Force Academy, Dundigul (located in Hyderabad), followed by operational training at various other schools.</p> <p> Advanced officer training for command positions is also conducted at the Defence Services Staff College; specialised advanced flight training schools are located at Bidar, Karnataka and Hakimpet, Telangana (also the location for helicopter training). Technical schools are found at a number of other locations.</p></td>
<td colspace="1"><img src="220px-Indian_Air_Force_Antonov_An-32.jpg"></td>
</tr>
<tr>
<ul>
<li><font size="4" color="blue"><u>Operational commands:</u></font>
<ul>
<li>Central Air Command (CAC), headquartered at Allahabad, Uttar Pradesh</li>
<li>Eastern Air Command (EAC), headquartered at Shillong, Meghalaya</li>
<li>Southern Air Command (SAC), headquartered at Thiruvananthapuram, Kerala</li>
<li>South Western Air Command (SWAC), headquartered at Gandhinagar, Gujara</li>
<li>Western Air Command (WAC), headquartered at New Delhi</li>
<li>Training Command (TC), headquartered at Bangalore, Karnataka</li>
<li>Maintenance Command (MC), headquartered at Nagpur, Maharashtra</li>
</ul>
</li>
<li><font size="4" color="blue"><u>Wings:</u></font>
<ul>
<li>A wing is a formation intermediate between a command and a squadron. It generally consists of two or three IAF squadrons and helicopter units, along with forward base support units (FBSU). FBSUs do not have or host any squadrons or helicopter units but act as transit airbases for routine operations. In times of war, they can become fully fledged air bases playing host to various squadrons. In all, about 47 wings and 19 FBSUs make up the IAF.[100][101] Wings are typically commanded by a group captain.</li>
</ul>
</li>
<li><font size="4" color="blue"><u>Squadrons and units:</u></font>
<ul>
<li>Squadrons are the field units and formations attached to static locations. Thus, a flying squadron or unit is a sub-unit of an air force station which carries out the primary task of the IAF. A fighter squadron consists of 18 aircraft; all fighter squadrons are headed by a commanding officer with the rank of wing commander.Some transport squadrons and helicopter units are headed by a commanding officer with the rank of group captain.</li>
</ul>
</li>
<li><font size="4" color="blue"><u>Flights:</u></font>
<ul>
<li>Flights are sub-divisions of squadrons, commanded by a squadron leader. Each flight consists of two sections.</li>
</ul>
</li>
<li><font size="4" color="blue"><u>Sections:</u></font>
<ul>
<li>The smallest unit is the section, led by a flight lieutenant. Each section consists of three aircraft.</li>
<li>Within this formation structure, IAF has several service branches for day-to-day operations. They are:
<ol type="i">
<li>Flying Branch</li>
<li>Technical Branch</li>
<li>Ground Branch</li>
</ol>
</li>
</ul>
</li>
<li><font size="4" color="blue"><u>Integrated Space Cell:</u></font>
<ul>
<li>An Integrated Space Cell, which will be jointly operated by all the three services of the Indian armed forces, the civilian Department of Space and the Indian Space Research Organisation (ISRO) has been set up to utilise more effectively the country's space-based assets for military purposes.</li>
<li>This command will leverage space technology including satellites. Unlike an aerospace command, where the air force controls most of its activities, the Integrated Space Cell envisages co-operation and co-ordination between the three services as well as civilian agencies dealing with space.</li>
</ul>
</tr>

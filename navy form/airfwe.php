<td colspan="3"><p>The Indian Air Force was established on 8 October 1932 in British India as an auxiliary air force of the Royal Air Force. The enactment of the Indian Air Force Act 1932 stipulated out their auxiliary status and enforced the adoption of the Royal Air Force uniforms, badges, brevets and insignia.</p><p> On 1 April 1933, the IAF commissioned its first squadron, No.1 Squadron, with four Westland Wapiti biplanes and five Indian pilots. The Indian pilots were led by British RAF Commanding officer Flight Lieutenant (later Air Vice Marshal) Cecil Bouchier.</p></td>

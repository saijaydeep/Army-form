<td><h2><b><i><u>Coast Guard Officers:</u></i></b></h2><td>
</tr>
<tr>
<td><p>The naming of ranks of officers in the Coast Guard is as same as rank of Central Armed Police Forces. Officers are commissioned in the Coast Guard in one of four branches, as either General-Duty officer, Pilot officer, Technical officer or Law officers. Lady Officers have two branches i.e. General-Duty Officer or Pilot Officer and serve on shore establishments/Air Stations/Headquarters. They are not deployed on board Indian Coast Guard ships. Currently, officers of Indian Coast Guard undergo Basic Military Training at the Indian Naval Academy, Ezhimala along with their counterparts of Indian Navy. This helps in the mutual interchange of Officers among these two sister services. While the Indian Coast Guard Academy is under construction in Azhikkal, Kannur district, Kerala.</p></td>
</tr>
<tr>
<td><u><font size="4" color="grey">General-Duty Officers:</font></u></td>
</tr>
<tr>
<td><p>The command of ships at sea can only be exercised by officers of the General-Duty (GD) branch. The key functions of a General-Duty Officer would be to operate weapons, sensors and different kinds of equipment on board a ship. The safety of the ship and the men would be GD officers responsibility.[29] All the District Commanders (COMDIS) and Commander of Coast Guard Region (COMCG) appointments are exercised by a GD Officer of the Indian Coast Guard. GD officers are graduates in Science or Engineering.</p></td>
</tr>
<tr>
<td><u><font size="4" color="grey">Pilot Officers:</font></u></td>
</tr>
<tr>
<td><p>Pilot Officers are also part of GD branch. A Pilot Officer gets an opportunity to work at shore Air Stations along the Indian coasts and also embark ships. ICG operates fixed wing aircraft for surveillance of the Exclusive Economic Zone. In addition, helicopters are embarked on Coast Guard Offshore Patrol Vessels (OPV) to provide local surveillance and perform search and rescue mission at sea.[29] Pilot officers are graduates in Science or Engineering.</p></td>
</tr>
<tr>
<td><u><font size="4" color="grey">Technical Officers:</font></u></td>
</tr>
<tr><p>Technical Officers are responsible for operation of advanced technology and sensor systems on board Coast Guard vessels and aircraft, as well as on shore installations. They also command the maintenance wings of the force. Technical Officers are graduates in Engineering.</p></tr>
<tr>
<td><u><font size="4" color="grey">Law Officers:</font></u></td>
</tr>
<tr>
<td><p>Law Officers act as legal advisers to their respective commanders. They represent the Indian Coast Guard in legal actions filed by or against the organisation. They also perform the duties of trial law officers in Coast Guard courts, convened to try delinquent Coast Guard personnel. The Directorate of Law at Coast Guard Headquarters is headed by a Deputy Inspector-General and is designated as the Chief Law Officer.  </p><td>
</tr>
<tr>
<td><u><font size="4" color="grey">Enrolled personal:</font></u></td>
</tr>
<tr>
<td><p>Enrolled personnel in the Coast Guard serve as either a yantrik (technician) or navik (sailor).</p></td>
</tr>
<tr>
<td>
<ul type="circle"><li>Yantriks are responsible for operating and maintaining mechanical, electrical or aeronautical equipment and systems on board the Coast Guard vessels and aircraft.</li> 
<li>Naviks may further serve in the General-Duty or Domestic branches. The General-Duty naviks serve as sailors, weapons systems operators, communication specialists, divers, etc. or in specific maritime or aviation support roles. Domestic branch naviks serve in roles such as stewards, cooks, etc. on board Coast Guard vessels.</li>
</ul>
</td>
</tr>
<tr>
<td><p>Enrolled personnel of Indian Coast Guard are trained along with Indian Naval sailors at the naval training establishment INS Chilka. All training undertaken by Coast Guard personnel is the same as those undertaken by sailors in the Indian Navy. All personnel are trained in operation of weapons systems in cases of emergency.</p></td>
</tr>

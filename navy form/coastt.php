<ul style="list-style-type:disc"><h3><u><i><b>Current Role:</b></i></u></h3></ul>
<p>The Indian Coast Guard's motto is Vayam Rakshamah,which translates from Sanskrit as "We Protect".

Missions of Indian Coast Guard:</p>
<li>Safety and protection of artificial islands, offshore terminals and other installations</li>
<li>Protection and assistance to fishermen and mariners at sea</li>
<li>Preservation and protection of marine ecology and environment including pollution control</li>
<li>Assistance to the Department of Customs and other authorities in anti-smuggling operations</li>
<li>Law enforcement in territorial as well as international waters</li>
<li>Scientific data collection and support</li>
<h3><b><i>additional responsibilities of the Indian Coast Guard:</i></b></h3>
<li>Offshore Security Coordination Committee (OSCC) – The Director-General of the Indian Coast Guard is the Chairman of OSCC constituted by the Ministry of Petroleum and Natural Gas (MoPNG). </li>
<li>Lead Intelligence Agency (LIA) – For coastal and sea borders</li>
</ul>

<h3><u><i><b>Leadership and Organisation:</b></i></u></h3>
</td>
</tr>
<tr>
<td><p>The Indian Coast Guard organisation is headed by the Director-General (DG ICG) who is located at Coast Guard Headquarters (CGHQ), New Delhi. At CGHQ, he is assisted by four Deputy Director-General's of the rank of Inspector-General, and other senior officers heading various staff divisions. </p><p>The Indian Coast Guard operates five regions. Each region is headed by an officer of the rank of Inspector-General. Each of the regions is further divided into multiple districts, typically covering a coastal state or a union territory.</p><td>

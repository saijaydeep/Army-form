<b><i></h1><u><font size="6">Indian Coast Guard:</font></u></h1></i></b>
<table border="0">
<tr>
<td colspan="3"><p>The Indian Coast Guard (ICG) protects India's maritime interests and enforces maritime law, with jurisdiction over the territorial waters of India, including its contiguous zone and exclusive economic zone. The Indian Coast Guard was formally established on 18 August 1978 by the Coast Guard Act, 1978 of the Parliament of India as an independent Armed force of India. It operates under the Ministry of Defence.</p><p>The Coast Guard works in close cooperation with the Indian Navy, the Department of Fisheries, the Department of Revenue (Customs) and the Central and State police forces.</p></td>

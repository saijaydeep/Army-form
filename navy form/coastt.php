<b><u><i></h1><font size="6">History:</font></h1></i></u></b>
<table>
<tr>
<td colspan="3"><p>The establishment of the Indian Coast Guard was first proposed by the Indian Navy to provide non-military maritime services to the nation.[6] In the 1960s, sea-borne smuggling of goods was threatening India's domestic economy. The Indian Customs Department frequently called upon the Indian Navy for assistance with patrol and interception in the anti-smuggling effort.</p><p>The Nagchaudhuri Committee was constituted with participation from the Indian Navy and the Indian Air Force to study the problem. In August 1971, the committee identified the requirement to patrol India's vast coastline, set up a registry of offshore fishing vessels in order to identify illegal activity, and establish a capable and well-equipped force to intercept vessels engaged in illegal activities. The committee also looked at the number and nature of the equipment, infrastructure and personnel required to provide those services.</p><p>The Indian Coast Guard conducts exercises with the other coast guards of the world. In May 2005, the ICG agreed to establish liaison links with Pakistan's Maritime Security Agency (PMSA). In 2006, the Indian Coast Guard conducted exercises with its Japanese and Korean counterparts.

After the 2008 Mumbai attacks, the Indian government initiated a programme to expand the ICG force, assets and infrastructure. The force aims to have 150 ships and 100 twin-engined aircraft by 2020 in its fleet.</p></td>


<b><i></h1><u><font size="6">mission and doctrine:</font></u></h1></i></b>
<table>
<tr>
<td colspace="3"><p>Initially, the army's main objective was to defend the nation's frontiers. However, over the years, the army has also taken up the responsibility of providing internal security, especially against insurgencies in Kashmir and the Northeast. The current combat doctrine of the Indian Army is based on effectively utilising holding formations and strike formations. In the case of an attack, the holding formations would contain the enemy and strike formations would counter-attack to neutralise enemy forces.</p><p> In the case of an Indian attack, the holding formations would pin enemy forces down, whilst the strike formations attack at a point of Indian choosing. The Indian Army is large enough to devote several corps to the strike role. Currently, the army is also looking at enhancing its special forces capabilities. With the role of India increasing, and the requirement for protection of India's interests in far-off countries become important, the Indian Army and Indian Navy are jointly planning to set up a marine brigade.</p></td>

<b><u><i></h1><font size="6">History:</font></h1></i></u></b>
<table>
<tr>
<td colspan="3"><p>A Military Department was created within the Government of the East India Company at Kolkata in the year 1776. Its main function was to sift and record orders relating to the Army that were issued by various Departments of the East India Company for the territories under its control.</p><p>With the Charter Act of 1833, the Secretariat of the Government of the East India Company was reorganised into four Departments, including a Military Department. The army in the Presidencies of Bengal, Bombay and Madras functioned as respective Presidency Armies until 1 April 1895 when they were unified into a single Indian Army.[14][15][16][17] For administrative convenience, it was divided into four commands at that point, namely Punjab (including the North West Frontier), Bengal, Madras (including Burma) and Bombay (including Sind, Quetta and Aden).</p>

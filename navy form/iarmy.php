</tr>
<tr>
<td><p>The Directorate of Military Intelligence (DMI) is the Intelligence arm of the Indian Army. The MI (as it is commonly referred to) was constituted in 1941 and was initially created to check corruption in the Army's own ranks. With time, its role has evolved into cross border intelligence, intelligence sharing with friendly nations, infiltrating insurgent groups and counter-terrorism. In the late 1970s, the MI was embroiled in the Samba spy scandal wherein three Indian Army officers were falsely implicated as Pakistani spies. The organisation has since emerged from the scandal as a prime Intelligence organisation of the Indian Army. As of 2012, the MI has seen many of its roles taken away in a turf war with the newly created National Technical Research Organisation and the Defence Intelligence Agency<p></td>
